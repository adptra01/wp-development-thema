  <script>
  (function() {
      document.querySelectorAll('.reveal-word').forEach(el => {
          const inner = document.createElement('span');
          inner.className = 'reveal-inner';
          inner.style.transitionDelay = el.style.transitionDelay || '0s';
          inner.textContent = el.textContent;
          el.textContent = '';
          el.appendChild(inner);
      });
      document.querySelectorAll('.word-reveal').forEach(el => {
          const observer = new IntersectionObserver((entries) => {
              entries.forEach(entry => {
                  if (entry.isIntersecting) {
                      el.classList.add('revealed');
                      observer.unobserve(el);
                  }
              });
          }, { threshold: 0.15 });
          observer.observe(el);
      });

      const initWebGL = () => {
          if (typeof THREE === 'undefined') return;
          const container = document.getElementById('webgl-container');
          if (!container || container.children.length > 0) return;

          const scene = new THREE.Scene();
          const camera = new THREE.PerspectiveCamera(45, container.clientWidth / container.clientHeight, 0.1, 1000);
          camera.position.z = 30;

          const renderer = new THREE.WebGLRenderer({ alpha: true, antialias: false });
          renderer.setSize(container.clientWidth, container.clientHeight);
          renderer.setPixelRatio(Math.min(window.devicePixelRatio, 2));
          container.appendChild(renderer.domElement);

          const geometry = new THREE.TorusKnotGeometry(10, 3, 200, 32);
          const particleMaterial = new THREE.PointsMaterial({
              color: 0x8B5CF6,
              size: 0.06,
              transparent: true,
              opacity: 0.25,
              blending: THREE.AdditiveBlending
          });

          const particles = new THREE.Points(geometry, particleMaterial);
          scene.add(particles);

          let mouseX = 0, mouseY = 0, targetX = 0, targetY = 0;
          let windowHalfX = window.innerWidth / 2;
          let windowHalfY = window.innerHeight / 2;

          const section = container.parentElement;
          if (section) {
              section.addEventListener('mousemove', (event) => {
                  mouseX = (event.clientX - windowHalfX) * 0.001;
                  mouseY = (event.clientY - windowHalfY) * 0.001;
              });
          }

          const clock = new THREE.Clock();
          const animate = () => {
              requestAnimationFrame(animate);
              const elapsedTime = clock.getElapsedTime();
              targetX = mouseX * 0.5;
              targetY = mouseY * 0.5;
              particles.rotation.y += 0.002;
              particles.rotation.x += 0.001;
              particles.rotation.x += 0.05 * (targetY - particles.rotation.x);
              particles.rotation.y += 0.05 * (targetX - particles.rotation.y);
              const scale = 1 + Math.sin(elapsedTime * 0.5) * 0.05;
              particles.scale.set(scale, scale, scale);
              renderer.render(scene, camera);
          };
          animate();

          window.addEventListener('resize', () => {
              if (!container.clientWidth) return;
              windowHalfX = window.innerWidth / 2;
              windowHalfY = window.innerHeight / 2;
              camera.aspect = container.clientWidth / container.clientHeight;
              camera.updateProjectionMatrix();
              renderer.setSize(container.clientWidth, container.clientHeight);
          });
      };

      if (document.readyState === 'complete') {
          initWebGL();
      } else {
          window.addEventListener('DOMContentLoaded', initWebGL);
      }
  })();
  </script>

